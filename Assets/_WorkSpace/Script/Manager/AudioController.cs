using UnityEngine;
using UnityEngine.SceneManagement;

public class AudioController : MonoBehaviour
{
    static AudioController instance { get; set; }

    [SerializeField]
    AudioSource _audioSorce;

    #region TitleSceneでの音声を保存する変数
    [SerializeField]
    AudioClip _startSEClip;
    [SerializeField]
    AudioClip _titleBGMClip;
    #endregion

    #region TutorialSceneでの音声を保存する変数
    [SerializeField]
    AudioClip _gameStartSEClip;
    #endregion

    #region PlaySceneでの音声を保存する変数
    [SerializeField]
    AudioClip _upperImpulseSEClip;
    [SerializeField]
    AudioClip _playBGMClip;
    #endregion

    #region GameClearSceneでの音声を保存する変数
    [SerializeField]
    AudioClip _restartGameClearSEClip;
    #endregion

    #region GameOverSceneでの音声を保存する変数
    [SerializeField]
    AudioClip _restartGameOverSEClip;
    #endregion
    // Start is called before the first frame update
    void Start()
    {
        _audioSorce.Stop();
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
            SceneManager.sceneLoaded += OnSceneLoaded;
        }
        else
        {
            Destroy(gameObject);
        }
    }

    void OnSceneLoaded(Scene scene, LoadSceneMode mode)
    {
        _audioSorce.Stop();

        #region TitleSceneでのBGMの処理
        if (scene.name == "TitleScene")
        {
            _audioSorce.clip = _titleBGMClip;
            _audioSorce.Play();
        }
        #endregion

        #region TutorialSceneでのBGMの処理
        if (scene.name == "TutorialScene")
        {
            // BGMなし
        }
        #endregion

        #region PlaySceneでのBGMの処理
        if (scene.name == "PlayScene")
        {
            _audioSorce.clip = _playBGMClip;
            _audioSorce.Play();
        }
        #endregion

        #region GameClearSceneでのBGMの処理
        if (scene.name == "GameClearScene")
        {
            // BGMなし（必要に応じてクリア用BGMを追加）
        }
        #endregion

        #region GameOverSceneでのBGMの処理
        if (scene.name == "GameOverScene")
        {
            // BGMなし（必要に応じてゲームオーバー用BGMを追加）
        }
        #endregion
    }

    void Update()
    {
        #region TitleSceneでのSEの処理
        if (SceneManager.GetActiveScene().name == "TitleScene")
        {
            if (Input.anyKeyDown)
            {
                _audioSorce.PlayOneShot(_startSEClip);
            }
        }
        #endregion

        #region TutorialSceneでのSEの処理
        if (SceneManager.GetActiveScene().name == "TutorialScene")
        {
            if (Input.GetKeyDown(KeyCode.Return))
            {
                _audioSorce.PlayOneShot(_gameStartSEClip);
            }
        }
        #endregion

        #region PlaySceneでのSEの処理
        if (SceneManager.GetActiveScene().name == "PlayScene")
        {
            if (Input.GetKeyDown(KeyCode.M))
            {
                _audioSorce.PlayOneShot(_upperImpulseSEClip);
            }
        }
        #endregion

        #region GameClearSceneでのSEの処理
        if (SceneManager.GetActiveScene().name == "GameClearScene")
        {
            if (Input.anyKeyDown)
            {
                _audioSorce.PlayOneShot(_restartGameClearSEClip);
            }
        }
        #endregion

        #region GameOverSceneでのSEの処理
        if (SceneManager.GetActiveScene().name == "GameOverScene")
        {
            if (Input.anyKeyDown)
            {
                _audioSorce.PlayOneShot(_restartGameOverSEClip);
            }
        }
        #endregion 
    }
}
